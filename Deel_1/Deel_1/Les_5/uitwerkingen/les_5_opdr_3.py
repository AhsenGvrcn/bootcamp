#Eerst de variabelen maken.
var1= float(input('Wat is de getal van variabele 1?'))
var2= float(input('Wat is de getal van variabele 2?'))
var3= float(input('Wat is de getal van variabele 3?'))
#Nu het gemiddelde bereken zoals je in wiskunde zou doen.
gemiddelde= (var1 + var2 + var3) / 3
print("Het gemiddelde is" , gemiddelde) 
#In plaats van + gebruik je een komma. Als je geen float bij input doet geeft het een ongeldige string, als je getallen wilt gebruiken in Python moet je de string geldig maken via float!
