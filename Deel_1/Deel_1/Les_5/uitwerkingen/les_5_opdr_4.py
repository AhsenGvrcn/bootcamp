#De formule voor het berekenen van het oppervlakte van een cirkel = straal * straal * pi, hierbij eerst de gegevens.. voor de omtrek is dat 2*straal*pi.
straal= float(input('Hoeveel is de straal van de cilinder?')) #Weer float voor getallen!
pi= 3.14159
#Invoeren van de eerste formule
oppervlakte= straal * straal * pi
print('De oppervlakte van een cirkel met straal ',straal , 'is' , oppervlakte)
#Invoeren van de tweede formule > 2*straal*pi
omtrek= 2*straal*pi
print('De omtrek van een cirkel is', omtrek)

